CREATE TABLE [dbo].[Strategies]
(
	[Id] INT PRIMARY KEY NOT NULL IDENTITY(1,1),
	[UserId] NVARCHAR(128) NOT NULL,
	[Name] NVARCHAR(100) NOT NULL,
	[MA1] NVARCHAR(5) NOT NULL,
	[MA2] NVARCHAR(5) NOT NULL,
	[Indicator] NVARCHAR(10) NOT NULL,
	[Interval] NVARCHAR(5) NOT NULL,
	[Range] NVARCHAR(5) NOT NULL
)
